package com.yzs.crm.workbench.bo;

public class TranValue {

    private String tranMoney;
    private String tranName;
    private String tranExpectedDate;
    private String tranStage;
    private String tranActivityId;

    public String getTranMoney() {
        return tranMoney;
    }

    public void setTranMoney(String tranMoney) {
        this.tranMoney = tranMoney;
    }

    public String getTranName() {
        return tranName;
    }

    public void setTranName(String tranName) {
        this.tranName = tranName;
    }

    public String getTranExpectedDate() {
        return tranExpectedDate;
    }

    public void setTranExpectedDate(String tranExpectedDate) {
        this.tranExpectedDate = tranExpectedDate;
    }

    public String getTranStage() {
        return tranStage;
    }

    public void setTranStage(String tranStage) {
        this.tranStage = tranStage;
    }

    public String getTranActivityId() {
        return tranActivityId;
    }

    public void setTranActivityId(String tranActivityId) {
        this.tranActivityId = tranActivityId;
    }
}
